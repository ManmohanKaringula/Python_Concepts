# The python calander module is used to create calander objects that provides various methods and attributes which help us work 
# with calander objects...
import calendar
cal= calendar.month(2020, 3)
print(cal)

# To print a whole year of a calendar we can use the prcal() method in python. The year for which we want to print the calander 
# should be passed to the prcal() method 
print(calendar.prcal(2022)) 