# List append operation 
lst=[1,2,3,4]
lst.append(5)
print(lst)
# List extend operation
str="Hello"
lst.extend(str)
print(lst)
# List insert operation
lst.insert(0,19)
print(lst)
# List remove operation
lst.remove(4)
print(lst)
# List pop operation
print(lst.pop(4), "element at index 4 is popped out and the next time you print the list the element is removed")
print(lst)

